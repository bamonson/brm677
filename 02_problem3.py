#Code to determine the value difference between 2 variables.
a=5
b=6
#Variables "a" and "b"" have values assigned of 5 and 6.  
if a>b:
	print('a is greater than b')
elif a<b:
	print('a is smaller than b')
else:
	print('a is equal to b')
#The commands of "if", "elif", and "else" are used to show if "a" is greater, smaller, or equal to "b".